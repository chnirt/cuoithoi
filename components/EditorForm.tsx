"use client";

import { useFormContext, useWatch } from "react-hook-form";
import {
  Form,
  FormField,
  FormItem,
  FormLabel,
  FormControl,
  FormMessage,
} from "@/components/ui/form";
import { Input } from "@/components/ui/input";
import { Button } from "@/components/ui/button";
import { format } from "date-fns";
import {
  Popover,
  PopoverContent,
  PopoverTrigger,
} from "@/components/ui/popover";
import { Calendar } from "@/components/ui/calendar";
import { ChevronDownIcon } from "lucide-react";
import { toast } from "sonner";
import { WeddingPageProps } from "@/types/wedding";
import { saveWedding } from "@/lib/firestore/weddings";
import { useUser } from "@clerk/nextjs";
import { motion } from "framer-motion";
import { fadeInScale } from "@/lib/animations";
import { MIN_LOADING_TIME } from "@/constants/loading";
import { GalleryEditor } from "./GalleryEditor";
import { useEffect } from "react";
import { generateGoogleCalendarLink } from "@/lib/calendar";
import { vi } from "date-fns/locale";

interface EditorFormProps {
  onSubmittingChange?: (submitting: boolean) => void;
  onSaved?: (slug: string) => void;
}

export default function EditorForm({
  onSubmittingChange,
  onSaved,
}: EditorFormProps) {
  const { user } = useUser();
  const form = useFormContext<WeddingPageProps>();

  const onSubmit = async (values: WeddingPageProps) => {
    if (!user?.id) return;

    onSubmittingChange?.(true); // b√°o parent ƒëang submit

    try {
      const savePromise = saveWedding({ ...values, userId: user.id });
      const minDelay = new Promise((resolve) =>
        setTimeout(resolve, MIN_LOADING_TIME)
      );

      // Ch·ªù c·∫£ 2 promise xong
      const slug = await Promise.all([savePromise, minDelay]).then(
        ([slug]) => slug
      );

      toast.success("Th√¥ng tin ƒë√£ ƒë∆∞·ª£c c·∫≠p nh·∫≠t!");
      onSaved?.(slug);

      return slug;
    } catch (error) {
      console.error("Error saving wedding:", error);
      toast.error("L∆∞u th√¥ng tin th·∫•t b·∫°i!");
    } finally {
      onSubmittingChange?.(false); // b√°o parent xong submit
    }
  };

  const handleReset = () => {
    form.reset();
    toast("ƒê√£ ƒë·∫∑t l·∫°i th√¥ng tin m·∫∑c ƒë·ªãnh");
  };

  const couple = useWatch({ control: form.control, name: "couple" });
  const event = useWatch({ control: form.control, name: "event" });

  const isPastWedding = event?.datetime
    ? new Date(event.datetime).getTime() < new Date().getTime()
    : false;
  // const isPastWedding = true;

  useEffect(() => {
    if (!couple || !event) return;

    const newUrl = generateGoogleCalendarLink({
      couple: {
        bride: couple.bride || "",
        groom: couple.groom || "",
      },
      event: {
        date: event.date || "",
        datetime: event.datetime || "",
        time: event.time || "",
        venue: event.venue || "",
        address: event.address || "",
      },
    });

    // üîç Ch·ªâ c·∫≠p nh·∫≠t n·∫øu URL thay ƒë·ªïi th·ª±c s·ª±
    const currentUrl = form.getValues("event.calendarUrl");
    if (currentUrl !== newUrl) {
      form.setValue("event.calendarUrl", newUrl, {
        shouldValidate: false,
        shouldDirty: true,
      });
    }
  }, [
    couple.bride,
    couple.groom,
    event.date,
    event.datetime,
    event.time,
    event.venue,
    event.address,
    form,
    couple,
    event,
  ]);
  return (
    <Form {...form}>
      <motion.form
        {...fadeInScale(0)}
        onSubmit={form.handleSubmit(onSubmit)}
        className="space-y-8 max-w-3xl mx-auto p-6"
      >
        {/* C·∫∑p ƒë√¥i */}
        <h2
          className="text-2xl font-semibold text-neutral-900 mb-4"
          style={{ fontFamily: "var(--font-heading)" }}
        >
          Th√¥ng Tin C·∫∑p ƒê√¥i
        </h2>

        <FormField
          control={form.control}
          name="couple.bride"
          rules={{ required: "T√™n c√¥ d√¢u kh√¥ng ƒë∆∞·ª£c ƒë·ªÉ tr·ªëng" }}
          render={({ field }) => (
            <FormItem>
              <FormLabel className="text-base font-medium">
                T√™n C√¥ D√¢u
              </FormLabel>
              <FormControl>
                <Input
                  placeholder="Nh·∫≠p t√™n c√¥ d√¢u"
                  className="h-11 text-base"
                  {...field}
                  disabled={isPastWedding}
                />
              </FormControl>
              <FormMessage />
            </FormItem>
          )}
        />

        <FormField
          control={form.control}
          name="couple.groom"
          rules={{ required: "T√™n ch√∫ r·ªÉ kh√¥ng ƒë∆∞·ª£c ƒë·ªÉ tr·ªëng" }}
          render={({ field }) => (
            <FormItem>
              <FormLabel className="text-base font-medium">
                T√™n Ch√∫ R·ªÉ
              </FormLabel>
              <FormControl>
                <Input
                  placeholder="Nh·∫≠p t√™n ch√∫ r·ªÉ"
                  className="h-11 text-base"
                  {...field}
                  disabled={isPastWedding}
                />
              </FormControl>
              <FormMessage />
            </FormItem>
          )}
        />

        {/* S·ª± ki·ªán */}
        <h2
          className="text-2xl font-semibold text-neutral-900 mb-4 mt-10"
          style={{ fontFamily: "var(--font-heading)" }}
        >
          Th√¥ng Tin S·ª± Ki·ªán
        </h2>

        <div className="grid grid-cols-1 sm:grid-cols-2 gap-6">
          <FormField
            control={form.control}
            name="event.datetime"
            rules={{ required: "Ng√†y c∆∞·ªõi kh√¥ng ƒë∆∞·ª£c ƒë·ªÉ tr·ªëng" }}
            render={({ field }) => (
              <FormItem>
                <FormLabel className="text-base font-medium">
                  Ng√†y C∆∞·ªõi
                </FormLabel>
                <Popover>
                  <PopoverTrigger asChild>
                    <FormControl>
                      <Button
                        variant="outline"
                        className="w-full justify-between h-11 text-base font-light"
                        disabled={isPastWedding}
                      >
                        {field.value
                          ? format(new Date(field.value), "dd/MM/yyyy")
                          : "Ch·ªçn ng√†y"}
                        <ChevronDownIcon className="ml-2 h-4 w-4" />
                      </Button>
                    </FormControl>
                  </PopoverTrigger>
                  <PopoverContent className="w-auto p-0 z-50 shadow-md">
                    <Calendar
                      mode="single"
                      selected={field.value ? new Date(field.value) : undefined}
                      onSelect={(date) => {
                        if (!date) return;

                        const d = new Date(date); // l·∫•y ng√†y m·ªõi t·ª´ calendar
                        if (field.value) {
                          const prev = new Date(field.value);
                          // gi·ªØ gi·ªù, ph√∫t, gi√¢y t·ª´ datetime c≈©
                          d.setHours(
                            prev.getHours(),
                            prev.getMinutes(),
                            prev.getSeconds(),
                            0
                          );
                        }

                        field.onChange(d.toISOString());
                      }}
                      locale={vi}
                      disabled={isPastWedding}
                    />
                  </PopoverContent>
                </Popover>
                <FormMessage />
              </FormItem>
            )}
          />

          <FormField
            control={form.control}
            name="event.datetime"
            rules={{ required: "Th·ªùi gian kh√¥ng ƒë∆∞·ª£c ƒë·ªÉ tr·ªëng" }}
            render={({ field }) => (
              <FormItem>
                <FormLabel className="text-base font-medium">
                  Th·ªùi Gian
                </FormLabel>
                <FormControl>
                  <Input
                    type="time"
                    value={
                      field.value
                        ? `${new Date(field.value)
                            .getHours()
                            .toString()
                            .padStart(2, "0")}:${new Date(field.value)
                            .getMinutes()
                            .toString()
                            .padStart(2, "0")}`
                        : "11:11"
                    }
                    onChange={(e) => {
                      if (!field.value) return;
                      const d = new Date(field.value);
                      const [h, m] = e.target.value.split(":").map(Number);
                      d.setHours(h, m, 0, 0);
                      field.onChange(d.toISOString());
                    }}
                    step={60}
                    className="h-11 text-base bg-background appearance-none [&::-webkit-calendar-picker-indicator]:hidden"
                    disabled={isPastWedding}
                  />
                </FormControl>
                <FormMessage />
              </FormItem>
            )}
          />
        </div>

        <FormField
          control={form.control}
          name="event.venue"
          rules={{ required: "T√™n ƒë·ªãa ƒëi·ªÉm kh√¥ng ƒë∆∞·ª£c ƒë·ªÉ tr·ªëng" }}
          render={({ field }) => (
            <FormItem>
              <FormLabel className="text-base font-medium">
                T√™n ƒê·ªãa ƒêi·ªÉm
              </FormLabel>
              <FormControl>
                <Input
                  placeholder="VD: Nh√† H√†ng Ti·ªác C∆∞·ªõi"
                  className="h-11 text-base"
                  {...field}
                  disabled={isPastWedding}
                />
              </FormControl>
              <FormMessage />
            </FormItem>
          )}
        />

        <FormField
          control={form.control}
          name="event.address"
          rules={{ required: "ƒê·ªãa ch·ªâ kh√¥ng ƒë∆∞·ª£c ƒë·ªÉ tr·ªëng" }}
          render={({ field }) => (
            <FormItem>
              <FormLabel className="text-base font-medium">ƒê·ªãa Ch·ªâ</FormLabel>
              <FormControl>
                <Input
                  placeholder="VD: 123 ƒê∆∞·ªùng B·∫•t K·ª≥, Th√†nh Ph·ªë"
                  className="h-11 text-base"
                  {...field}
                  disabled={isPastWedding}
                />
              </FormControl>
              <FormMessage />
            </FormItem>
          )}
        />

        <FormField
          control={form.control}
          name="event.mapUrl"
          render={({ field }) => (
            <FormItem>
              <FormLabel className="text-base font-medium">
                Li√™n K·∫øt B·∫£n ƒê·ªì
              </FormLabel>
              <FormControl>
                <Input
                  placeholder="https://maps.google.com/..."
                  className="h-11 text-base"
                  {...field}
                  disabled={isPastWedding}
                />
              </FormControl>
              <FormMessage />
            </FormItem>
          )}
        />

        <FormField
          control={form.control}
          name="event.calendarUrl"
          // render={({ field }) => (
          //   <FormItem>
          //     <FormLabel className="text-base font-medium">
          //       Li√™n K·∫øt L·ªãch
          //     </FormLabel>
          //     <FormControl>
          //       <Input
          //         placeholder="https://calendar.google.com/..."
          //         className="h-11 text-base"
          //         {...field}
          //       />
          //     </FormControl>
          //     <FormMessage />
          //   </FormItem>
          // )}
          render={({ field }) => <input type="hidden" {...field} />}
        />

        {/* B·ªô S∆∞u T·∫≠p ·∫¢nh */}
        <h2
          className="text-2xl font-semibold text-neutral-900 mb-4 mt-10"
          style={{ fontFamily: "var(--font-heading)" }}
        >
          B·ªô S∆∞u T·∫≠p ·∫¢nh
        </h2>

        <FormField
          control={form.control}
          name="gallery"
          render={({ field }) => (
            <GalleryEditor field={field} disabled={isPastWedding} />
          )}
        />

        {/* Buttons */}
        <div className="flex flex-col sm:flex-row gap-4 pt-6">
          <Button
            type="submit"
            className="flex-1 bg-[#b9a27f] hover:bg-[#a89370] h-11 text-base font-medium"
            disabled={form.formState.isSubmitting || isPastWedding}
          >
            {form.formState.isSubmitting ? "ƒêang l∆∞u..." : "L∆∞u Thay ƒê·ªïi"}
          </Button>
          <Button
            variant="secondary"
            type="button"
            onClick={handleReset}
            className="flex-1 h-11 text-base font-medium"
            disabled={isPastWedding}
          >
            ƒê·∫∑t L·∫°i
          </Button>
        </div>
      </motion.form>
    </Form>
  );
}
